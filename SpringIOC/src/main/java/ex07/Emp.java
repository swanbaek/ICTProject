package ex07;

public class Emp {
	private String dept;
	private String name;
	private int salary;
	
	public Emp() {
		
	}

	public Emp(String dept, String name, int salary) {
		super();
		this.dept = dept;
		this.name = name;
		this.salary = salary;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSalary() {
		return salary;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	//setter, getter---
	

}//////////////////////////////////
